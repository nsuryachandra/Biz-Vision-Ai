const formatBusinessTitle = (ideaText) => {
  if (!ideaText) return "Startup Feasibility Report";
  let text = ideaText.trim();
  
  // Strip common startup sentence prefixes
  text = text.replace(/^(i\s+)?want\s+to\s+(start|open|create|build|launch|run|make|have|do)\s+(a\s+)?/i, "");
  // Strip leading and trailing quotes or periods
  text = text.replace(/^["']|["']$/g, "").replace(/\.+$/, "").trim();
  
  // Format specific keywords into professional phrases
  text = text.replace(/\bpure\s+veg\b/i, "Pure Vegetarian");
  text = text.replace(/\bveg\b/i, "Vegetarian");
  text = text.replace(/\bhotel\b/i, "Hotel");
  text = text.replace(/\brestaurant\b/i, "Restaurant");
  text = text.replace(/\bcafe\b/i, "Cafe");
  text = text.replace(/\bshop\b/i, "Store");
  
  // Capitalize each word (Title Case)
  text = text.split(/\s+/).map(word => {
    if (!word) return "";
    return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
  }).join(" ");
  
  if (!text.endsWith("Feasibility Report") && !text.endsWith("Startup Feasibility Report")) {
    return `${text} Startup Feasibility Report`;
  }
  return text;
};

