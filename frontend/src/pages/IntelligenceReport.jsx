const formatCurrencyToINR = (val) => {
  if (!val) return "N/A";
  let str = String(val).trim();
  
  // If it already has Indian Rupee symbol, return it
  if (str.includes('₹')) {
    return str;
  }
  
  // Convert K/k or M/m suffixes
  str = str.replace(/\$(\d+(?:\.\d+)?)\s*[kK]\b/g, (m, p1) => `$${parseFloat(p1) * 1000}`);
  str = str.replace(/\$(\d+(?:\.\d+)?)\s*[mM]\b/g, (m, p1) => `$${parseFloat(p1) * 1000000}`);
  
  // If no currency symbols but has digits, add $ prefix to trigger conversion
  if (!str.includes('$') && /^\d/.test(str)) {
    str = str.replace(/(\b\d[\d,]*\b)/g, '$$$1');
  }
  
  // Convert USD to INR
  str = str.replace(/\$([\d,]+(?:\.\d+)?)/g, (match, p1) => {
    const num = parseFloat(p1.replace(/,/g, ''));
    if (!isNaN(num)) {
      const inrValue = num * 83;
      return '₹' + Math.round(inrValue).toLocaleString('en-IN');
    }
    return '₹' + p1;
  });
  
  str = str.replace(/\$/g, '₹');
  return str;
};

const CircularGauge = ({ label, value, ringColor, bgColor }) => {
  const [displayValue, setDisplayValue] = useState(0);
  const ringRef = useRef(null);
  
  const radius = 28;
  const circumference = 2 * Math.PI * radius;

  useEffect(() => {
    const obj = { val: 0 };
    // Animating number count up
    gsap.to(obj, {
      val: value,
      duration: 1.5,
      ease: 'power2.out',
      onUpdate: () => {
        setDisplayValue(Math.round(obj.val));
      }
    });

    // Animating circle drawing
    if (ringRef.current) {
      const targetOffset = circumference - (value / 100) * circumference;
      gsap.fromTo(ringRef.current, 
        { strokeDashoffset: circumference },
        { strokeDashoffset: targetOffset, duration: 1.5, ease: 'power2.out' }
      );
    }
  }, [value, circumference]);

  return (
    <motion.div
      whileHover={{ scale: 1.03, y: -2 }}
      className={cn("bg-slate-50/40 border border-slate-100 rounded-2xl p-5 flex flex-col items-center justify-center text-center space-y-3.5 shadow-sm transition-all duration-300", bgColor)}
    >
      <div className="relative flex items-center justify-center w-16 h-16">
        <svg className="w-16 h-16 transform -rotate-90">
          <circle cx="32" cy="32" r={radius} className="text-slate-100" strokeWidth="4.5" stroke="currentColor" fill="transparent" />
          <circle 
            ref={ringRef}
            cx="32" 
            cy="32" 
            r={radius} 
            className={`${ringColor} transition-all`} 
            strokeWidth="5" 
            strokeDasharray={circumference} 
            strokeLinecap="round" 
            stroke="currentColor" 
            fill="transparent" 
          />
        </svg>
        <span className="absolute text-sm font-extrabold text-slate-800">{displayValue}%</span>
      </div>
      <span className="text-xs font-bold uppercase tracking-wider text-slate-500 leading-tight">{label}</span>
    </motion.div>
  );
};

