const parseJSON = (raw, defaultValue) => {
  if (!raw) return defaultValue;
  try {
    let clean = raw.trim();
    if (clean.startsWith("```")) {
      clean = clean.replace(/^```(?:json)?\s*/i, "").replace(/\s*```$/i, "");
    }
    return JSON.parse(clean);
  } catch (e) {
    return defaultValue;
  }
};

